package proyecto_final;

public class Amarre {
	private int numamarre;
	private int lecturaagua;
	private int lecturaluz;
	
	public Amarre(int numamarre, int lecturaagua, int lecturaluz) {
		this.numamarre = numamarre;
		this.lecturaagua = lecturaagua;
		this.lecturaluz = lecturaluz;
	}

	public int getNumamarre() {
		return numamarre;
	}

	public void setNumamarre(int numamarre) {
		this.numamarre = numamarre;
	}

	public int getLecturaagua() {
		return lecturaagua;
	}

	public void setLecturaagua(int lecturaagua) {
		this.lecturaagua = lecturaagua;
	}

	public int getLecturaluz() {
		return lecturaluz;
	}

	public void setLecturaluz(int lecturaluz) {
		this.lecturaluz = lecturaluz;
	}
	
	
}
